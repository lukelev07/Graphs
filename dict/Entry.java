package dict;

/**
 *  A class for dictionary entries.
 *
 *  DO NOT CHANGE THIS FILE.  It is part of the interface of the
 *  Dictionary ADT.
 **/

public class Entry {

  protected Object key;
  protected Object value;

  /**
  * @return the object that represents the key gets returned
  */
  public Object key() {
    return key;
  }

  /**
  * @return the object that represents the key gets returned
  */
  public Object value() {
    return value;
  }

}
